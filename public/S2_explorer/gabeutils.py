def normalize(band, max_, min_, eps=0.0, sqrt_=False, clip_=True):
    band = np.array(band, "float")
    norm = (np.clip(band, min_, max_) - min_) / (max_ - min_ + eps * np.ones_like(band))
    if sqrt_:
        norm = np.sqrt(norm)
    if clip_:
        norm = np.clip(norm, 0, 1)
    return norm


def color_dict_256(cmap_name):
    cmap = plt.get_cmap(cmap_name)
    color_list = [
        [int(255 * cmap(el)[0]), int(255 * cmap(el)[1]), int(255 * cmap(el)[2])]
        for el in range(256)
    ]
    return dict(zip(list(range(256)), color_list))


def arr_to_cmap(data, cmap="plasma", norm_range=[0, 0.7]):
    color_dict = color_dict_256(cmap)
    import numpy as np

    data = data.astype(float)
    if norm_range:
        norm_data = (np.clip(data, norm_range[0], norm_range[1]) - norm_range[0]) / (
            norm_range[1] - norm_range[0]
        )
        norm_data = np.clip(norm_data, 0, 1)
    else:
        norm_data = np.clip(data, 0, 1)
    norm_data255 = (norm_data * 255).astype("uint8")
    mapped_colors = np.array([color_dict[val] for val in norm_data255.flat])
    return mapped_colors.reshape(data.shape + (3,)).astype("uint8").transpose(2, 0, 1)


